/**
 * Plantasonic Design System — Official code generators.
 *
 * Scaffolds that follow Design System conventions automatically: token-driven,
 * framework-agnostic HTML-string render functions, accessible markup, and a
 * matching metadata record so generated elements register themselves.
 *
 * Each generator returns {@link GeneratedFile}s (path + content) so callers
 * (CLI, build tools, AI agents) can write them wherever appropriate.
 */
import type { ShellVariant } from '../shell/types.ts';

export interface GeneratedFile {
  path: string;
  content: string;
  description?: string;
}

function pascal(name: string): string {
  return name
    .replace(/[-_\s]+/g, ' ')
    .split(' ')
    .filter(Boolean)
    .map((w) => w.charAt(0).toUpperCase() + w.slice(1))
    .join('');
}

function camel(name: string): string {
  const p = pascal(name);
  return p.charAt(0).toLowerCase() + p.slice(1);
}

function kebab(name: string): string {
  return name
    .replace(/([a-z0-9])([A-Z])/g, '$1-$2')
    .replace(/[\s_]+/g, '-')
    .toLowerCase();
}

export interface GenerateComponentOptions {
  name: string;
  purpose?: string;
  category?: string;
  variants?: string[];
}

/** Generate a token-driven component render function + metadata record. */
export function generateComponent(options: GenerateComponentOptions): GeneratedFile[] {
  const fn = camel(options.name);
  const Name = pascal(options.name);
  const cls = `ds-c-${kebab(options.name)}`;
  const category = options.category ?? 'controls';
  const variants = options.variants ?? [];
  const variantType = variants.length ? variants.map((v) => `'${v}'`).join(' | ') : 'string';

  const component = `/**
 * ${Name} — generated by plantasonic-design-system generateComponent().
 * Token-driven, framework-agnostic HTML string. No hardcoded values.
 */
import { classList, escapeHtml, type Size } from './types.ts';

export type ${Name}Variant = ${variantType};

export type ${Name}Options = {
  label: string;
  variant?: ${Name}Variant;
  size?: Size;
  id?: string;
  class?: string;
};

export function ${fn}(options: ${Name}Options): string {
  const variant = options.variant${variants.length ? ` ?? '${variants[0]}'` : ''};
  const cls = classList(
    '${cls}',
    variant && \`${cls}--\${variant}\`,
    options.size && options.size !== 'md' && \`${cls}--\${options.size}\`,
    options.class,
  );
  const idAttr = options.id ? \` id="\${escapeHtml(options.id)}"\` : '';
  return \`<div class="\${cls}"\${idAttr}>\${escapeHtml(options.label)}</div>\`;
}
`;

  const metadata = `import { defineComponent } from 'plantasonic-design-system/ai';

export const ${fn}Metadata = defineComponent({
  id: 'component.${fn}',
  name: '${Name}',
  kind: 'component',
  export: '${fn}',
  version: '0.1.0',
  purpose: ${JSON.stringify(options.purpose ?? `${Name} component.`)},
  category: '${category}',
  status: 'experimental',
  source: 'plantasonic-design-system/components',
  supportedThemes: ['dark', 'light'],
  variants: ${JSON.stringify(variants)},
  props: [
    { name: 'label', type: 'string', required: true },
    { name: 'variant', type: '${Name}Variant' },
    { name: 'size', type: "'sm' | 'md' | 'lg'", default: 'md' },
  ],
  accessibility: { notes: 'Review roles/ARIA before promoting to stable.' },
  supportedLayouts: ['*'],
});
`;

  return [
    { path: `src/components/${kebab(options.name)}.ts`, content: component, description: 'Component render function' },
    { path: `src/ai/contrib/${kebab(options.name)}.meta.ts`, content: metadata, description: 'Component metadata record' },
  ];
}

export interface GenerateLayoutOptions {
  name: string;
  purpose?: string;
  variant?: ShellVariant;
  regions: string[];
}

/** Generate a layout metadata record describing regions and slots. */
export function generateLayout(options: GenerateLayoutOptions): GeneratedFile[] {
  const id = `layout.${kebab(options.name)}`;
  const regions = options.regions.map(
    (r) => `    { name: '${kebab(r)}', description: '${pascal(r)} region.' }`,
  );
  const content = `import { defineLayout } from 'plantasonic-design-system/ai';

export const ${camel(options.name)}Layout = defineLayout({
  id: '${id}',
  name: '${pascal(options.name)}',
  kind: 'layout',
  variant: '${options.variant ?? 'standard'}',
  version: '0.1.0',
  purpose: ${JSON.stringify(options.purpose ?? `${pascal(options.name)} layout.`)},
  category: 'application',
  status: 'experimental',
  source: 'plantasonic-design-system/shell',
  supportedThemes: ['dark', 'light'],
  regions: [
${regions.join(',\n')},
  ],
  supportedComponents: ['*'],
});
`;
  return [{ path: `src/ai/contrib/${kebab(options.name)}.layout.ts`, content, description: 'Layout metadata record' }];
}

